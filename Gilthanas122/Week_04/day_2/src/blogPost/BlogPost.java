package blogPost;

public class BlogPost {
    String authorName;
    String title;
    String publicationDate;
    String text;


    public BlogPost(String authorName, String title, String publicationDate, String text) {
        this.authorName = authorName;
        this.title = title;
        this.publicationDate = publicationDate;
        this.text = text;
    }
}


